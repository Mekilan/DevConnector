const mongoose = require('mongoose');

// Create Schema object from mongoose

const userSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  avatar: {
    type: String,
  },
  date: {
    type: Date,
    default: Date.now(),
  },
});

// Export user model
module.exports = User = mongoose.model('users', userSchema);
